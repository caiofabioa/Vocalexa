    public void imprimiCadastro() throws JRException {
        
        String driver = "org.postgresql.Driver";
        String user   = "postgres";
        String senha = "postgres";
        String url   = "jdbc:postgresql://localhost:5432/vocalexa";
        
        try
        {
            Class.forName(driver);
            
            Connection con = (Connection) DriverManager.getConnection(url, user, senha);
            
            String query = "SELECT * FROM vocalexa_funcionario, vocalexa_exames "
                    + "WHERE vocalexa_funcionario.funcionario_rg = '"
                    + JTF_busca_rg.getText()+"' AND vocalexa_exames.ex_data = '"
                    + JTF_dataevento.getText()+"'";
            
            PreparedStatement cmd;
            
            cmd = con.prepareStatement(query);
            
            
            ResultSet rs;
            
            rs = cmd.executeQuery();
            
            
            JRResultSetDataSource jrRS = new JRResultSetDataSource( rs );
            
            JasperPrint jasperPrint = JasperFillManager.fillReport (
                    "C:/Program Files/Vocalexa/Exame_Completo.jasper", new HashMap(), jrRS);
                    JasperViewer.viewReport(jasperPrint, false);

        }
        catch (ClassNotFoundException | SQLException ex)
        {
            System.err.print(ex.getMessage());
        }
        catch (JRException ex){
            String erro = ex.getMessage();
            JOptionPane.showMessageDialog(this, erro, "Erro ao gerar relatório", ERROR);
        }
        
    }

